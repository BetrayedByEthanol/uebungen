var eingabe = prompt ("Geben Sie eine Zahl ein")  //variable eingabe als pop-up Eingabe

if (eingabe > 10) {  //Wenn "Eingabe" größer oder gleich 10 ist dann führe ... aus
    alert ("Die Zahl ist größer als 10") //alert = pop-up
}

else if (eingabe < 10) {  //Wenn das nich zustimmt führe ... aus
    alert("Die Zahl ist nicht größer als 10")  //alert = pop-up
}

else if (eingabe ==10) { //Wenn das nich zustimmt führe ... aus
    alert("Die Zahl ist gleich 10") //alert =pop-up
}

else {
    alert("Die Eingabe ist ungültig")
}